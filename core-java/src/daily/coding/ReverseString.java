package daily.coding;

public class ReverseString {

	public static void main(String[] args) {
		// defining a string
		
		    String name="anil kumar";
		    
		    // i want to reverse the string 
		    
		    for(int i=name.length()-1;i>=0;i--) {
		    	
		    	   System.out.print(name.charAt(i));
		    }

	}

}
